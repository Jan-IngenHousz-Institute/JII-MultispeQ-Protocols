def _analyze( _data ):
  """
  Macro for data evaluation artifacts on measurements caused by actinic light ground loops
  data on PhotosynQ.org
  by: David M. Kramer
  created: 2017-05-09 @ 18:15:27
  """  
        
  ## Define the output object here 
  graphs = False

  output = {} ## dictionary to hold results
  n_sets = len(_data["set"])

  # {\"Light 1\":\"530nm, green, main\"\
  # ,\"Light 2\":\"655nm red, main)\"\
  # ,\"Light 3\":\"590nm amber, main)\"\
  # ,\"Light 4\":\"448nm blue, main)\"\
  # ,\"Light 5\":\"950nm NIR, main)\"\
  # ,\"Light 6\":\"950nm NIR, clamp)\"\
  # ,\"Light 7\":\"655 red, clamp\"\
  # ,\"Light 8\":\"850nm NIR, clamp\"\
  # ,\"Light 9\":\"730nm far red, clamp\"\
  # ,\"Light 10\":\"810nm NIR, clamp\"\
  # ,\"Detector 1\":\"700nm - 1150nm, main\"\
  # ,\"Detector 3\":\"400nm - 700nm, clamp\"\
  # ,\"Ambient Temperature, Humidity, Pressure\":\"thp\"\
  # ,\"Ambient Temperature, Humidity, Pressure 2\":\"thp2\"\
  # ,\"Contactless Temperature\":\"contactless_temp\"\
  # ,\"Accelerometer\":\"compass_and_angle\"\
  # ,\"Magnetometer\":\"compass_and_angle\"\
  # ,\"Hall Effect Sensor\":\"thickness\"\
  # ,\"PAR light sensor\":\"light_intensity\"}"

  #       FOR actinic LED #2 
  #                           Assuming we use LED#2 as actinic
  #                           led#  det1  det3 
  #                           1     2     1
  #                           2     9     9  # 9 means use some generic correction 
  #                           3     2     1
  #                           4     2     1
  #                           5     2     1
  #                           6     3     9
  #                           7     4     9
  #                           8     3     9
  #                           9     5     9
  #                           10    6     9

  bleedArrays = {}

  MapLedToDet = [[2,9,2,2,2,3,4,5,6,7],[1,9,1,1,1,9,9,9,9,9]]  ## the first dimension is the det#, the second is the LED#

  bleedArrays[2]= MapLedToDet


  #       FOR actinic LED #9 
  #                           Assuming we use LED#2 as actinic
  #                           led#  det1  det3
  #                           1     0     1
  #                           2     9     9  # 9 means use some generic correction 
  #                           3     2     1
  #                           4     2     1
  #                           5     2     1
  #                           6     3     9
  #                           7     4     9
  #                           8     3     9
  #                           9     5     9
  #                           10    6     9

  MapLedToDet_act9 = [[8,9,8,8,9,10,11,12,9,13],[14,9,14,14,14,9,9,9,9,9]] ## the first dimension is the det#, the second is the LED# 

  bleedArrays[9]= MapLedToDet_act9



  x = _data["set"][0]


  bleed_correction = [1,0]


  Extr = []
  
  ## set upper limits (+/1) for testing of each LED with each actinic light
  Xbounds_for_act_2 = [[30,20,20,20,20,30,20,20,30],[20,20,20,20,20,30,20,20,20,30]]

  Extr.append(Xbounds_for_act_2)


  Xbounds_for_act_9 = [[30,30,30,30,30,30,20,20,20,30],[50,20,30,20,20,30,20,20,20,30]]

  Extr.append(Xbounds_for_act_9)




  act_settings = _data["v_arrays"][0]

  for i in range(len(act_settings)):  ## if direct DAC setting, mulitple by -1
    if act_settings[i] < 0:
      act_settings[i] = -1 * act_settings[i]

  mLEDs = [1,6,10,3]
  mDetectors = [1,0,0,0]
  aLEDs = _data["v_arrays"][2]

  nMeasuringLEDS = len(mLEDs)

  mLedNames = {1:"530nm", 2:"655nm", 3:"590nm", 4:"488nm", 5:"940nm", 6:"b940nm", 7:"b655nm", 8:"b850nm", 9:"b730nm", 10:"b810nm"}

  nSettings = len(act_settings)

  toDevice=""

  for i in range(15):
    toDevice += "bleed3+%s+0+0+0+0+0+0+0+0+" % i


  issues = 0

  for aLEDindex in range(len(aLEDs)):
    actBleed = []
    
    b = nSettings * aLEDindex
    e = nSettings * (1 + aLEDindex)
      
    t = GetProtocolByLabel( "bc0", _data, True )[b:e]
    actBleed.append(t)
    
    # output["trace_%s" % aLEDindex] = t["data_raw"] #TODO: This is not working, not sure why it is in the js code like this

    t = GetProtocolByLabel( "bc1", _data, True )[b:e]
    actBleed.append(t)

    

    actBleedTrace = {}
      
    subPoints = _data["v_arrays"][1][1]
      
    LEDs = _data["v_arrays"][2] ## set up array for LEDs tested. Note: this does not include the baseline
      
      
    for abi in range(len(actBleed)): ## cycle through the repeats of the entire experiment, each with different actinic light intensities. 
      tname = []
      offset = {}
      for ledidx in range(len(mLEDs)):
        tn = "actBleed_%s_aLED_%s_det_%s_bleed_%s" % (mLedNames[mLEDs[ledidx]], aLEDs[aLEDindex],  mDetectors[ledidx], actBleed[abi][0]["bleed_correction"])
        tname.append(tn)
        actBleedTrace[tn] = []
        offset[tn] = []

      actbleed = actBleed[abi]  ## set actbleed to the array of traces in this run of the set

      for ledidx in range(len(mLEDs)):
        numIntensities = len(actbleed)
        for i in range(numIntensities):  ## extract traces for each detector, subtract the baseline
          t = actbleed[i]["data_raw"][ledidx::nMeasuringLEDS]
          tm = np.mean(t[0:subPoints])
          t = np.array(t) - tm ## subtract off the baseline
          actBleedTrace[tname[ledidx]].append(t)
          averageOffset = np.mean(t[int(subPoints)+3 : 2*subPoints])
          offset[tname[ledidx]].append(averageOffset)
          # if actbleed[i]["bleed_correction"] == 1:  #TODO: Seems like it is not used
          #   Xbounds = Extr[aLEDindex]
          #   b = Xbounds[aLEDindex][mLEDs[ledidx]-1]

        if graphs == True:
          output["offset_%s" %tname[ledidx]] = offset[tname[ledidx]]

        ledN = mLEDs[ledidx]
        detN = mDetectors[ledidx]
        bleedArray = bleedArrays[aLEDs[aLEDindex]]
        if actbleed[0]["bleed_correction"] == 0:
            toDevice+="bleed3+%s+" % bleedArray[detN][ledN-1] ## MapLedToDet[mDetectors[ledidx]-1][mLEDs[ledidx]-1];
            for j in range(8):
              toDevice += "%s+" % np.round(10 * offset[tname[ledidx]][j], 0)


  output["toDevice"] = toDevice

  if issues == 0:
    output["status"] = " Test OK"
  else:
    output["status"] = " WARNING: %s found! Try sending calibration data to instrument." % issues
    output["toDevice"] = toDevice

  output["toDevice"] += "setCalTime+2+setCalOK+2+0+"

  return output


